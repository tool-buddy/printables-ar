namespace ToolBuddy.PrintablesAR.Sound
{
    /// <summary>
    /// The enumeration of sound effects used in the application.
    /// </summary>
    public enum Sounds
    {
        /// <summary>
        /// Sound for when an object is placed successfully.
        /// </summary>
        SuccessfulPlacement,
        
        /// <summary>
        /// Sound for when an object placement fails.
        /// </summary>
        FailedPlacement,
        
        /// <summary>
        /// Sound for when bidirectional dragging is unlocked.
        /// </summary>
        DraggingUnlocked,
        
        /// <summary>
        /// Sound for when an object is being dragged.
        /// </summary>
        Dragging,
        
        /// <summary>
        /// Sound for when a UI button is clicked.
        /// </summary>
        ButtonClicked
    }
}